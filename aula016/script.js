let frutas = ["Uva", "Morango", "Laranja"]

// Adiciona no ultimo index
frutas.push("Banana")

console.log(frutas)
// Remove o último index 
frutas.pop()

console.log(frutas)
// Remove o primeiro index
frutas.shift()
console.log(frutas)

// Insere no primeiro index,

frutas.unshift("Macarrão")
console.log(frutas)

// Checa se existe
console.log(frutas.includes("Pera"))
console.log(frutas.includes("Morango"))

